package Döngülerle_Çalışmak;

public class While_Döngüsü {
    public static void main(String[] args) {

        int i = 1;

        while (i < 10) {

            System.out.print(i+",");
            i++;
        }
        System.out.println(" ");
        System.out.println("1. while Döngüsü Sonu");

        int j=1;
        while (j<10){

            System.out.println("Teksaylar="+j);
            j+=2;
        }
    }
}
